create extension if not exists pgcrypto;

create table if not exists public.households (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  created_at timestamptz not null default now()
);

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  login_id text not null unique check (login_id ~ '^[A-Za-z0-9._-]{1,12}$'),
  display_name text not null,
  role text not null default 'user' check (role in ('admin', 'user')),
  household_id uuid references public.households(id) on delete set null,
  is_active boolean not null default true,
  created_at timestamptz not null default now()
);

create table if not exists public.phases (
  code text primary key check (code ~ '^[0-9]{2}$'),
  name text not null,
  age_label text not null,
  sort_order integer not null,
  is_active boolean not null default true
);

create table if not exists public.categories (
  code text primary key check (code ~ '^[0-9]{2}$'),
  name text not null,
  sort_order integer not null,
  is_active boolean not null default true
);

create table if not exists public.foods (
  id uuid primary key default gen_random_uuid(),
  scope text not null check (scope in ('global', 'household')),
  household_id uuid references public.households(id) on delete cascade,
  code text not null check (code ~ '^[0-9]{7}$'),
  phase_code text not null references public.phases(code),
  category_code text not null references public.categories(code),
  serial integer not null check (serial between 1 and 999),
  name text not null,
  is_active boolean not null default true,
  created_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (
    (scope = 'global' and household_id is null)
    or
    (scope = 'household' and household_id is not null)
  )
);

create unique index if not exists foods_global_code_idx
  on public.foods(code)
  where household_id is null;

create unique index if not exists foods_household_code_idx
  on public.foods(household_id, code)
  where household_id is not null;

create table if not exists public.children (
  id uuid primary key default gen_random_uuid(),
  household_id uuid not null references public.households(id) on delete cascade,
  number text not null check (number ~ '^[0-9]{2}$'),
  name text not null,
  created_at timestamptz not null default now(),
  unique (household_id, number)
);

create table if not exists public.food_records (
  child_id uuid not null references public.children(id) on delete cascade,
  food_id uuid not null references public.foods(id) on delete cascade,
  first_date date,
  second_date date,
  first_memo text not null default '',
  second_memo text not null default '',
  first_reaction boolean not null default false,
  second_reaction boolean not null default false,
  updated_at timestamptz not null default now(),
  primary key (child_id, food_id)
);

create table if not exists public.audit_logs (
  id bigint generated by default as identity primary key,
  actor_id uuid references auth.users(id) on delete set null,
  action text not null,
  detail jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.profiles
    where id = auth.uid()
      and role = 'admin'
      and is_active = true
  );
$$;

create or replace function public.current_household_id()
returns uuid
language sql
stable
security definer
set search_path = public
as $$
  select household_id
  from public.profiles
  where id = auth.uid()
    and is_active = true
  limit 1;
$$;

